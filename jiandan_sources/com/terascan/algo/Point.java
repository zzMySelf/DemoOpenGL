package com.terascan.algo;

public class Point {
    public float x;
    public float y;

    public Point() {
    }

    public float getX() {
        return this.x;
    }

    public float getY() {
        return this.y;
    }

    public void setX(float f) {
        this.x = f;
    }

    public void setY(float f) {
        this.y = f;
    }

    public Point(float f, float f2) {
        this.x = f;
        this.y = f2;
    }
}
